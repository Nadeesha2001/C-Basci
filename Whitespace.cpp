#include<iostream>
using namespace std;

int main(){

    cout << "How to Use Whitespace."<< endl;
    cout << "(1) Newline : \\n" << endl;
    cout << "Hello \n world" << endl;

    cout << "(2) tab : \\t" << endl;
    cout << "Hello\tWorld" << endl;

    cout << "(3) backspace : \\b" << endl;
    cout << "Hello\bworld" << endl;

    cout << "(4) form Feed : \\f" << endl;
    cout << "HelloWor\fld" << endl;

    cout << "(5) alert : \\a" << endl;
    cout << "He\alloWorld " << endl;

    cout << "(6) Single Quote : \\'" << endl;
    cout << "Tom\'s car is beautiful." << endl;

    cout << "(7) double Quote : \\\"" << endl;
    cout << "\"Hello World\"" << endl;

    cout << "(8) Question Mark : \\?" << endl;
    cout << "How are You\?" << endl;

    cout << "(9) backslash : \\" << endl;
    cout << "He \\ llo\\ world\\" << endl;
    return 0;

}